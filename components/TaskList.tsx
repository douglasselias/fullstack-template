import { Task } from '@/entities'
import { SingleTask } from '@/components'

export function TaskList(props: Props) {
  const { tasks } = props

  return (
    <ul>
      {tasks.map((task) => (
        <SingleTask key={task.id} task={task} />
      ))}
    </ul>
  )
}

type Props = {
  tasks: Task[]
}
